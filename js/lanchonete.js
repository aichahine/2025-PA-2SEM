// Aplicação do switch-case para o menu de uma lanchonete

var lanche="5";
switch(lanche){

    case "1":
    console.log("Pitbull - dois hambúrgueres, bacon e cheddar");
    break;
    
    case "2":
    console.log("Chihuahua - mini hambúrguer");
    break;
    
    case "3":
    console.log("Daschund - cachorro quente");
    break;
    
    case "4":
    console.log("SRD - Pão francês, hambúrguer, ovo, bacon e salsicha")
    break;

    default:
    console.log("Todos os lanches acompanham batatas fritas");
    
}