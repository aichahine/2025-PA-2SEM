// Aplicação do switch-case para identificar se o dia é útil ou fim de semana

var dia="domingo";
switch(dia){
    case"domingo":
    console.log("É fim de semana");
    break;

    case"segunda":
    console.log("É dia útil");
    break;
    
    case"terça":
    console.log("É dia útil");
    break;
    
    case"quarta":
    console.log("É dia útil");
    break;
    
    case"quinta":
    console.log("É dia útil");
    break;
    
    case"sexta":
    console.log("É dia útil");
    break;
    
    case"sábado":
    console.log("É fim de semana");  
    break; 

    default:
    console.log("Não existe dia ruim, o ruim é ter que trabalhar");  
}